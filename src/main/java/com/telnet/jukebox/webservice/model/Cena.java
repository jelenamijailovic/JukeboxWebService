package com.telnet.jukebox.webservice.model;

public class Cena {

	private int id;
	private int kolicina;

	public Cena() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getKolicina() {
		return kolicina;
	}

	public void setKolicina(int kolicina) {
		this.kolicina = kolicina;
	}

}
